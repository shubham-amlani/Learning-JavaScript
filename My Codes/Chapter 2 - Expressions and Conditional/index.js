//Switch Case statements
let x='pencil';
switch(x){
    case 'oranges':
        console.log("Price of oranges is 100 rs/kg");
        break;
    case 'mangoes':
        console.log("Price of mangoes is 60 rs/kg");
        break;
    case 'pencil':
        console.log("Price of pencil is 5 rs/piece");
        break;
    default:
        console.log("Invalid Item");
}